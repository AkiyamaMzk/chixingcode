package chixing.dayNull;

import java.util.Comparator;

public class SimpleProduct implements Comparable{
    private String name;
    private double price;

    public SimpleProduct(){}
    
    public SimpleProduct(String name, double price) {
        this.name = name;
        this.price = price;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public double getPrice() {
        return price;
    }
    public void setPrice(double price) {
        this.price = price;
    }
    @Override
    public String toString() {
        return "SimpleProduct [name=" + name + ", price=" + price + "]";
    }
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        long temp;
        temp = Double.doubleToLongBits(price);
        result = prime * result + (int) (temp ^ (temp >>> 32));
        return result;
    }
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        SimpleProduct other = (SimpleProduct) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (Double.doubleToLongBits(price) != Double.doubleToLongBits(other.price))
            return false;
        return true;
    }

    @Override
    public int compareTo(Object o) {
        SimpleProduct that=(SimpleProduct) o;
        return Comparator
                .comparing(SimpleProduct::getName)
                .thenComparingDouble(SimpleProduct::getPrice)
                .compare(this,that);
    }
    
}
